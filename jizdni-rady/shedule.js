
function timeToInt(str) {
  let parts = str.split(':')
  return 60 * parseInt(parts[0]) + parseInt(parts[1])
}

function intToTime(num) {
  return Math.floor(num / 60) + ':' + ((num % 60) + '').padStart(2, '0')
}

